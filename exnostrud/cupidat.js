// Declaration and initialization of the leaves array
var leaves = [];

// Adding elements to the array
leaves.push("green");
leaves.push("yellow");
leaves.push("red");

console.log(leaves); // Output: ["green", "yellow", "red"]

// Accessing elements in the array
console.log(leaves[0]); // Output: "green"
console.log(leaves.length); // Output: 3
